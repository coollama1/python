
def print_top_line():
    print("^ _ ^ _ ^^ _ ^ _ ^^ _ ^ _ ^^ _ ^ _ ^^")

def print_i_line():
    print("i        i        i        i        i")

def print_row():
    print_top_line()
    print_i_line()
    print_i_line()
    print_i_line()
    print_i_line()

def print_square():
    print_row()
    print_row()
    print_row()
    print_row()
    print_top_line()

print_square() 